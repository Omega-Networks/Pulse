import Foundation

enum SwiftDataError: Error {
    case wrongDataFormat(error: Error)
    case missingData
    case creationError
    case batchInsertError
    case batchDeleteError
    case persistentHistoryChangeError
    case invalidPropertiesType
    case relationshipMappingError(error: Error)
    case unexpectedError(error: Error)
}

extension SwiftDataError: LocalizedError {
    var errorDescription: String? {
        switch self {
        case .wrongDataFormat(let error):
            return NSLocalizedString("Could not digest the fetched data. \(error.localizedDescription)", comment: "")
        case .missingData:
            return NSLocalizedString("Found and will discard an object missing a valid code, magnitude, place, or time.", comment: "")
        case .creationError:
            return NSLocalizedString("Failed to create a new object.", comment: "")
        case .batchInsertError:
            return NSLocalizedString("Failed to execute a batch insert request.", comment: "")
        case .batchDeleteError:
            return NSLocalizedString("Failed to execute a batch delete request.", comment: "")
        case .persistentHistoryChangeError:
            return NSLocalizedString("Failed to execute a persistent history change request.", comment: "")
        case .invalidPropertiesType:
            return NSLocalizedString("Failed to execute with an invlaid properties type.", comment: "")
        case .relationshipMappingError(let error):
            return NSLocalizedString("Failed to map relationships. \(error.localizedDescription)", comment: "")
        case .unexpectedError(let error):
            return NSLocalizedString("Received unexpected error. \(error.localizedDescription)", comment: "")
        }
    }
}

extension SwiftDataError: Identifiable {
    var id: String? {
        errorDescription
    }
}
